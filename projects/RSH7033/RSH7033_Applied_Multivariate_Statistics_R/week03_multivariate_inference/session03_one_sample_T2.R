source("setup/install_packages.R")

library(ICSNP)

############################################################
# Load Data (Example: Iris)
############################################################

data(iris)

X <- iris[,1:4]

############################################################
# Hypothesized Mean Vector
############################################################

mu0 <- colMeans(X)

############################################################
# Hotelling's T² Test
############################################################

hotelling.test(X, mu = mu0)

