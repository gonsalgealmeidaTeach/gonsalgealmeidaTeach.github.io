# Week 3: Paired Multivariate Test
############################################################

source("setup/install_packages.R")

library(ICSNP)

############################################################
# Simulated Paired Data
############################################################

set.seed(123)

X1 <- matrix(rnorm(40), ncol = 2)
X2 <- X1 + matrix(rnorm(40, mean = 0.5), ncol = 2)

############################################################
# Differences
############################################################

D <- X1 - X2

############################################################
# Test Mean Difference = 0
############################################################

hotelling.test(D, mu = c(0,0))

############################################################
# Interpretation
############################################################

# Paired version reduces to one-sample test on differences
