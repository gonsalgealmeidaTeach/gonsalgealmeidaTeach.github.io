source("setup/install_packages.R")

library(MVN)

############################################################
# Load Data
############################################################

data(iris)

X <- iris[,1:4]

############################################################
# Multivariate Normality Test
############################################################

mvn(X, mvnTest = "hz")

############################################################
# Covariance Matrix
############################################################

cov(X)

cor(X)

############################################################
# Interpretation
############################################################

# Multivariate normality is a key assumption
# Covariance structure influences test statistics
