############################################################
# Create two matrices
############################################################

mat1 <- matrix(c(4, 3, 4, 5, 3, 5, 3, 22, 2, 1), ncol = 2)
mat1

mat2 <- matrix(c(5, 3, 3, 5, 3, 4, 3, 45, 77, 3), ncol = 2)
mat2


############################################################
# Check dimensions
############################################################

ncol(mat2)
nrow(mat2)


############################################################
# Addition of matrices
############################################################

mat3 <- mat1 + mat2
mat3


############################################################
# Element-wise multiplication
############################################################

mat4 <- mat1 * mat2
mat4


############################################################
# Matrix multiplication
############################################################

mat5 <- t(mat1) %*% mat2
mat5

mat6 <- mat1 %*% t(mat2)
mat6

# Note:
# t(A) %*% B is not the same as A %*% t(B)


############################################################
# Cross-product operations
############################################################

mat7 <- crossprod(mat1, mat2)
mat7

mat8 <- tcrossprod(mat1, mat2)
mat8


############################################################
# Matrix inverse
############################################################

# Check rank deficiency
nrow(mat7) - qr(mat7)$rank

# Compute inverse
mat7_inv <- solve(mat7)

mat7_inv %*% mat7
mat7 %*% mat7_inv


############################################################
# Identity matrix
############################################################

diag(x = 1, 2, 2)

mat7 %*% diag(x = 1, 2, 2)


############################################################
# Case where inverse does NOT exist
############################################################

nrow(mat8) - qr(mat8)$rank

# Attempt inverse
solve(mat8)


############################################################
# Generalized inverse
############################################################

library(MASS)

ginv(mat8)


############################################################
# Matrix form of multiple regression
############################################################

# Create matrix of predictors

reg_mat <- matrix(sample(seq(0,9), 60, replace = TRUE), ncol = 4)

reg_mat


############################################################
# Add intercept column
############################################################

inter <- rep(1, nrow(reg_mat))

full <- cbind(inter, reg_mat)


############################################################
# Create response variable
############################################################

resp_var <- sample(seq(0,9), 15, replace = TRUE)


############################################################
# Regression coefficients using matrix algebra
############################################################

# Formula:
# β = (X'X)^(-1) X'Y

betas <- solve(t(full) %*% full) %*% (t(full) %*% resp_var)

betas


############################################################
# Compare with lm()
############################################################

coefficients(lm(resp_var ~ ., data = data.frame(full)))
