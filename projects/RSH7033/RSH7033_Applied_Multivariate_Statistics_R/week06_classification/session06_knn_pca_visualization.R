############################################################
# Week 6: KNN Visualization using PCA
############################################################

source("setup/install_packages.R")

library(class)
library(rgl)

############################################################
# Load Data
############################################################

placekick <- read.csv("data/placekick.csv")

X <- placekick[,-9]
Y <- placekick[,9]

############################################################
# PCA
############################################################

pca <- princomp(X, cor = TRUE)

scores <- predict(pca)

############################################################
# KNN (K = 4)
############################################################

set.seed(123)

pred_cv <- knn.cv(scale(X), Y, k = 4)

############################################################
# 2D Plot
############################################################

col <- ifelse(pred_cv == 1, "red", "black")

plot(scores[,1], scores[,2],
     col = col,
     xlab = "PC1", ylab = "PC2",
     main = "KNN Classification (PCA Space)")

abline(h = 0)
abline(v = 0)

############################################################
# 3D Plot
############################################################

plot3d(scores[,1], scores[,2], scores[,3],
       col = col,
       xlab = "PC1", ylab = "PC2", zlab = "PC3")
